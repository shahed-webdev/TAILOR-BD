        // ── Schema column existence — cached ──────────────────────────────────────
        private async Task<bool> ColumnExistsAsync(string table, string column)
        {
            var key = $"schema_col_{table}_{column}";
            if (_cache.TryGetValue(key, out bool exists)) return exists;

            using var con = _context.CreateConnection();
            exists = await con.ExecuteScalarAsync<int>(
                "SELECT COUNT(*) FROM INFORMATION_SCHEMA.COLUMNS WHERE TABLE_NAME=@t AND COLUMN_NAME=@c",
                new { t = table, c = column }) > 0;

            _cache.Set(key, exists, _schemaCacheDuration);
            return exists;
        }

        // ── Cached SQL expressions for Invoice columns ────────────────────────────
        private async Task<(string totalAmountExpr, string paidAmountExpr, string isAutoGenExpr)> GetInvoiceExprsAsync()
        {
            bool hasTotalAmount = await ColumnExistsAsync("Invoice", "TotalAmount");
            bool hasPaidAmount  = await ColumnExistsAsync("Invoice", "PaidAmount");
            bool hasIsAutoGen   = await ColumnExistsAsync("Invoice", "IsAutoGenerated");

            var totalAmountExpr = hasTotalAmount
                ? "ISNULL(NULLIF(inv.TotalAmount,0), (SELECT ISNULL(SUM(Amount),0) FROM Invoice_Line WHERE InvoiceID=inv.InvoiceID))"
                : "(SELECT ISNULL(SUM(Amount),0) FROM Invoice_Line WHERE InvoiceID=inv.InvoiceID)";

            var paidAmountExpr = hasPaidAmount
                ? "inv.PaidAmount"
                : "ISNULL((SELECT SUM(Amount) FROM Invoice_Payment_Record WHERE InvoiceID=inv.InvoiceID),0)";

            var isAutoGenExpr = hasIsAutoGen ? "inv.IsAutoGenerated" : "0";

            return (totalAmountExpr, paidAmountExpr, isAutoGenExpr);
        }

        // ── GET /api/invoice/institutions ─────────────────────────────────────────
        /// <summary>Returns all institutions with their invoice/expiry summary.</summary>
        [HttpGet("institutions")]
        public async Task<ActionResult> GetInstitutions(
            [FromQuery] string? search = null,
            [FromQuery] string? validation = null,
            [FromQuery] string? expiry = null)
        {
            try
            {
                using var con = _context.CreateConnection();
                var sql = @"
                    SELECT
                        i.InstitutionID,
                        i.InstitutionName,
                        i.Phone,
                        i.Address,
                        i.Validation,
                        i.Expire_Date,
                        i.Renew_Amount,
                        i.PackageID,
                        p.PackageName,
                        p.Interval AS PackageInterval,
                        ISNULL(l.UserName,'') AS UserName,
                        ISNULL(SUM(
                            ISNULL(NULLIF(inv.TotalAmount,0), il_sum.LineTotal)
                            - ISNULL(inv.PaidAmount,0)
                            - ISNULL(inv.Discount,0)
                        ),0) AS TotalDue,
                        COUNT(inv.InvoiceID) AS TotalInvoices,
                        MAX(inv.CreateDate) AS LastInvoiceDate
                    FROM Institution i
                    LEFT JOIN Package  p   ON i.PackageID = p.PackageID
                    LEFT JOIN LIU      l   ON i.InstitutionID = l.InstitutionID AND l.Category='Admin'
                    LEFT JOIN Invoice  inv ON i.InstitutionID = inv.InstitutionID AND inv.PaymentStatus <> 'Paid'
                    LEFT JOIN (
                        SELECT InvoiceID, ISNULL(SUM(Amount),0) AS LineTotal
                        FROM Invoice_Line
                        GROUP BY InvoiceID
                    ) il_sum ON inv.InvoiceID = il_sum.InvoiceID
                    WHERE 1=1
                ";
                if (!string.IsNullOrWhiteSpace(validation))
                    sql += " AND i.Validation = @validation";
                if (!string.IsNullOrWhiteSpace(search))
                    sql += " AND (i.InstitutionName LIKE @search OR i.Phone LIKE @search OR l.UserName LIKE @search)";
                if (expiry == "expired")
                    sql += " AND i.Expire_Date < GETDATE()";
                else if (expiry == "expiring")
                    sql += " AND i.Expire_Date BETWEEN GETDATE() AND DATEADD(DAY,15,GETDATE())";

                sql += @" GROUP BY i.InstitutionID, i.InstitutionName, i.Phone,
                                   i.Address, i.Validation, i.Expire_Date, i.Renew_Amount,
                                   i.PackageID, p.PackageName, p.Interval, l.UserName
                          ORDER BY i.InstitutionID DESC";

                var parameters = new DynamicParameters();
                if (!string.IsNullOrWhiteSpace(validation))
                    parameters.Add("validation", validation);
                if (!string.IsNullOrWhiteSpace(search))
                    parameters.Add("search", "%" + search + "%");

                var rows = await con.QueryAsync(sql, parameters);

                // Project to camelCase so JS field names match exactly
                var data = rows.Select(r => new
                {
                    institutionId = (int)r.InstitutionID,
                    institutionName = (string)(r.InstitutionName ?? ""),
                    phone = (string)(r.Phone ?? ""),
                    address = (string)(r.Address ?? ""),
                    validation = (string)(r.Validation ?? ""),
                    expire_Date = r.Expire_Date == null ? (DateTime?)null : (DateTime)r.Expire_Date,
                    renewAmount = r.Renew_Amount == null ? 0.0 : Convert.ToDouble(r.Renew_Amount),
                    packageId = r.PackageID == null ? 0 : (int)r.PackageID,
                    packageName = (string)(r.PackageName ?? ""),
                    packageInterval = r.PackageInterval == null ? (int?)null : (int)r.PackageInterval,
                    userName = (string)(r.UserName ?? ""),
                    totalDue = Convert.ToDouble(r.TotalDue ?? 0),
                    totalInvoices = (int)(r.TotalInvoices ?? 0),
                    lastInvoiceDate = r.LastInvoiceDate == null ? (DateTime?)null : (DateTime)r.LastInvoiceDate,
                }).ToList();

                return Ok(new { success = true, data });
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Error fetching institutions for invoice");
                return StatusCode(500, new { success = false, message = ex.Message });
            }
        }

        // ── GET /api/invoice/list ──────────────────────────────────────────────────
        /// <summary>Returns invoice list, optionally filtered by institution.</summary>
        [HttpGet("list")]
        public async Task<ActionResult> GetInvoices(
            [FromQuery] int? institutionId = null,
            [FromQuery] string? status = null,
            [FromQuery] int page = 1,
            [FromQuery] int pageSize = 20)
        {
            try
            {
                var (totalAmountExpr, paidAmountExpr, isAutoGenExpr) = await GetInvoiceExprsAsync();

                using var con = _context.CreateConnection();

                var sql = $@"
                    SELECT
                        inv.InvoiceID,
                        inv.InstitutionID,
                        i.InstitutionName,
                        inv.IssuDate,
                        inv.EndDate,
                        inv.Invoice_For,
                        {totalAmountExpr} AS TotalAmount,
                        {paidAmountExpr}  AS PaidAmount,
                        ISNULL(inv.Discount,0) AS Discount,
                        inv.PaymentStatus,
                        inv.CreateDate,
                        {isAutoGenExpr}   AS IsAutoGenerated,
                        ISNULL(l.UserName,'') AS UserName
                    FROM Invoice inv
                    JOIN Institution i ON inv.InstitutionID = i.InstitutionID
                    LEFT JOIN LIU l ON inv.InstitutionID = l.InstitutionID AND l.Category='Admin'
                    WHERE 1=1";

                if (institutionId.HasValue) sql += " AND inv.InstitutionID = @institutionId";
                if (!string.IsNullOrWhiteSpace(status)) sql += " AND inv.PaymentStatus = @status";
                sql += " ORDER BY inv.CreateDate DESC OFFSET @offset ROWS FETCH NEXT @pageSize ROWS ONLY";

                var rows = await con.QueryAsync(sql, new
                {
                    institutionId,
                    status,
                    offset   = (page - 1) * pageSize,
                    pageSize
                });

                var countSql = "SELECT COUNT(*) FROM Invoice inv WHERE 1=1" +
                    (institutionId.HasValue ? " AND inv.InstitutionID=@institutionId" : "") +
                    (!string.IsNullOrWhiteSpace(status) ? " AND inv.PaymentStatus=@status" : "");
                var total = await con.ExecuteScalarAsync<int>(countSql, new { institutionId, status });

                var data = rows.Select(r => new
                {
                    invoiceID       = (int)r.InvoiceID,
                    institutionID   = (int)r.InstitutionID,
                    institutionName = (string)(r.InstitutionName ?? ""),
                    issuDate        = r.IssuDate  == null ? (DateTime?)null : (DateTime)r.IssuDate,
                    endDate         = r.EndDate   == null ? (DateTime?)null : (DateTime)r.EndDate,
                    invoice_For     = (string)(r.Invoice_For ?? ""),
                    totalAmount     = r.TotalAmount == null ? 0.0 : Convert.ToDouble(r.TotalAmount),
                    paidAmount      = r.PaidAmount  == null ? 0.0 : Convert.ToDouble(r.PaidAmount),
                    discount        = r.Discount    == null ? 0.0 : Convert.ToDouble(r.Discount),
                    paymentStatus   = (string)(r.PaymentStatus ?? "Due"),
                    createDate      = r.CreateDate == null ? (DateTime?)null : (DateTime)r.CreateDate,
                    isAutoGenerated = r.IsAutoGenerated != null && Convert.ToInt32(r.IsAutoGenerated) == 1,
                    userName        = (string)(r.UserName ?? ""),
                }).ToList();

                return Ok(new { success = true, data, total, page, pageSize });
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Error fetching invoices");
                return StatusCode(500, new { success = false, message = ex.Message });
            }
        }

