        // ── POST /api/invoice/create ───────────────────────────────────────────────
        /// <summary>Manually create invoices for selected institutions.</summary>
        [HttpPost("create")]
        public async Task<ActionResult> CreateInvoices([FromBody] CreateInvoiceRequest req)
        {
            if (req.InstitutionIds == null || !req.InstitutionIds.Any())
                return BadRequest(new { success = false, message = "No institutions selected" });
            if (!req.Lines.Any())
                return BadRequest(new { success = false, message = "At least one invoice line required" });

            try
            {
                var created = 0;
                var errors = new List<string>();

                foreach (var insId in req.InstitutionIds)
                {
                    try
                    {
                        await _billing.CreateInvoiceForInstitutionAsync(
                            insId,
                            req.InvoiceFor,
                            req.IssuDate ?? DateTime.Today,
                            req.EndDate,
                            req.Discount,
                            req.Lines,
                            isAutoGenerated: false);
                        created++;
                    }
                    catch (Exception ex)
                    {
                        errors.Add($"InstitutionID {insId}: {ex.Message}");
                    }
                }

                return Ok(new
                {
                    success = true,
                    message = $"{created} invoice(s) created successfully",
                    created,
                    errors
                });
            }
            catch (Exception ex)
            {
                _logger.LogError(ex, "Error creating invoices");
                return StatusCode(500, new { success = false, message = ex.Message });
            }
        }

